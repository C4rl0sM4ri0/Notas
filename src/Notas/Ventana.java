    private void botonguardardatosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonguardardatosActionPerformed
        // TODO add your handling code here:
         if (contador < clase.getNroelementos()) {
            String Nombre_est = txtnombre.getText();
            clase.setNombre(Nombre_est, contador);

            String asignatura = txtasignatura.getText();
            clase.setAsignatura(asignatura, contador);

            float nota = Float.parseFloat(txtnota.getText());
            clase.setNota(contador, nota);

            contador++;
         
         }
           txtnombre.setText(null);
           txtasignatura.setText(null);
           txtnota.setText(null);
        
      
    }//GEN-LAST:event_botonguardardatosActionPerformed

