    private void botonbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonbuscarActionPerformed
        // TODO add your handling code here:
        String buscando = buscador.getText();
        int arreglo = clase.buscar(buscando);
       
        if (arreglo != -1) {
            String Nombre_est = clase.getNombre(arreglo);
            nombreencontrado.setText(Nombre_est);

            String asignatura = clase.getAsignatura(arreglo);
            asignaturaencontrada.setText(asignatura);
            
            float nota = clase.getNota(arreglo);
            notaencontrada.setText(String.valueOf(nota));
            
        }else {
                 JOptionPane.showMessageDialog(null,"Estudiante no encontrado");
                    }
         buscador.setText(null);
    }//GEN-LAST:event_botonbuscarActionPerformed

